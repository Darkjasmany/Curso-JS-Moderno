// Object literal
const producto2 = {
    nombre: 'Monitor 30"',
    precio: 500,
    disponible: true,
};

console.log(producto2);

console.log(producto2.nombre);
console.log(producto2.precio);
console.log(producto2.disponible);

// Otra forma de acceder a las propiedades de un objeto
console.log(producto2["nombre"]);
console.log(producto2["precio"]);
console.log(producto2["disponible"]);
