// Veamos como ir incrementando un valor de 1 en 1 o en determinada cantidad

// Incluir incremento por 1 y menos 1

let puntaje = 5;
puntaje++; //muestra y luego incrementa 5
++puntaje; //incrementa y luego muestra 6

puntaje--; //muestra y luego decrementa 5
--puntaje; //descrementa y luego muestra 4

puntaje += 3; //8
puntaje -= 3; //2

console.log(puntaje);
